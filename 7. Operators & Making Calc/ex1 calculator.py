#Calculator

n1=5 
n2=3

#Addition
print("Addition:",n1, "+", n2, "=", n1+n2)

#Subtraction
print("Subtraction:",n1, "-", n2, "=", n1-n2)

#Multiplication
print("Multiplication:",n1, "*", n2, "=", n1*n2)

#Division
print("Division:",n1, "/", n2, "=", n1/n2)

#Modulus (Remainder)
print("Modulus:",n1, "%", n2, "=", n1%n2)

#Floor Division (Round Up)
print("Floor Division:",n1, "//", n2, "=", n1//n2)

#Exponential
print("Exponential:",n1, "**", n2, "=", n1**n2)